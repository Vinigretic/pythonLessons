# 1) Git створення нового репозиторія
# - Створюємо репозиторій в веб версії
# - Копіюємо силку
# - Вводимо наступні команди:
#
# 1) git init
#
# 2) git remote add origin <link>
#
# 3) git add .
# - Робимо коміт в IDE
#
# - Робимо пуш в IDE
#
# 2) Оновлення існуючого репозиторія:
# - git add .
# - робимо коміт
# - робимо пуш
#
# Додаткова команда:
# git status